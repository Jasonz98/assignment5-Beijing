      <h2>History and development</h2>
      <h3>Creation</h3>
      <p>In early December 1891, Canadian Dr. James Naismith, a physical education professor and instructor at the International Young Men's Christian Association Training School (YMCA) (today, Springfield College) in Springfield, Massachusetts, was trying to keep his gym class active on a rainy day. He sought a vigorous indoor game to keep his students occupied and at proper levels of fitness during the long New England winters. After rejecting other ideas as either too rough or poorly suited to walled-in gymnasiums, he wrote the basic rules and nailed a peach basket onto a 10-foot (3.0 m) elevated track. In contrast with modern basketball nets, this peach basket retained its bottom, and balls had to be retrieved manually after each "basket" or point scored; this proved inefficient, however, so the bottom of the basket was removed, allowing the balls to be poked out with a long dowel each time.</p>

      <p>The peach baskets were used until 1906 when they were finally replaced by metal hoops with backboards. A further change was soon made, so the ball merely passed through. Whenever a person got the ball in the basket, his team would gain a point. Whichever team got the most points won the game.[10] The baskets were originally nailed to the mezzanine balcony of the playing court, but this proved impractical when spectators in the balcony began to interfere with shots. The backboard was introduced to prevent this interference; it had the additional effect of allowing rebound shots. Naismith's handwritten diaries, discovered by his granddaughter in early 2006, indicate that he was nervous about the new game he had invented, which incorporated rules from a children's game called duck on a rock, as many had failed before it.</p>

      <p>Frank Mahan, one of the players from the original first game, approached Naismith after the Christmas break, in early 1892, asking him what he intended to call his new game. Naismith replied that he hadn't thought of it because he had been focused on just getting the game started. Mahan suggested that it be called "Naismith ball", at which he laughed, saying that a name like that would kill any game. Mahan then said, "Why not call it basketball?" Naismith replied, "We have a basket and a ball, and it seems to me that would be a good name for it." The first official game was played in the YMCA gymnasium in Albany, New York, on January 20, 1892, with nine players. The game ended at 1–0; the shot was made from 25 feet (7.6 m), on a court just half the size of a present-day Streetball or National Basketball Association (NBA) court.</p>
      
      
      <h3>Development</h3>
      
      <div class="row">
       <div class="col-sm">
       <h4>YMCA, U.S. Army spread development</h4>
       <p>The YMCA had a major role in spreading basketball throughout the United States, Canada, and the world. In 1893, Mel Rideout arranged the first European match in Paris, in Montmartre. At the same time, Bob Gailey went to Tientsin, China, Duncan Patton to India, Genzabaro Ishikawa to Japan, and C. Hareek to Persia.The First World War broke out in 1914, and the U.S. Army started fighting in Europe in 1917. During World War I, the American Expeditionary Force took basketball wherever it went. Together with the troops, there were hundreds of physical education teachers who knew basketball. Naismith also spent two years with the YMCA in France in that period.</p>
       </div>
       
       <div class="col-sm">
       <h4>American colleges lead the way</h4>
       <p>The greatest level of early basketball activity outside of YMCAs was seen in American colleges. The first known U.S. college to field a basketball team against an outside opponent was Vanderbilt University, which played against the local YMCA in Nashville, Tennessee, on February 7, 1893. The second recorded instance of an organized college basketball game was Geneva College's game against the New Brighton YMCA on April 8, 1893, in Beaver Falls, Pennsylvania, which Geneva won 3–0.</p>

      <p>By 1897 the U.S. Amateur Athletic Union (AAU) had taken over oversight of basketball activity from the YMCA. In April 1905, representatives of fifteen colleges separately took over control of the college game, creating the collegiate "Basket Ball Rule Committee." The Committee was in turn absorbed into the predecessor of the National Collegiate Athletic Association (NCAA) in 1909. The extremely popular NCAA Men's Basketball Tournament was started in 1939.</p>
        </div>
      </div>
      
      <div class="row">
       <div class="col-sm">
       <h4>Formation of FIBA</h4>
       <p>World basketball was growing, but it was on June 18, 1932 that a real international organization was formed, to coordinate tournaments and teams: that day, Argentina, Czechoslovakia, Greece, Italy, Latvia, Portugal, Romania and Switzerland founded the International Basketball Federation (Fédération internationale de basketball amateur, FIBA) in Geneva. Its work was fundamental for the first inclusion of basketball in the Berlin Olympic Games in 1936. The first Olympic title was won by the U.S. national team: Sam Balter, Ralph Bishop, Joe Fortenberry, Tex Gibbons, Francis Johnson, Carl Knowles, Frank Lubin, Art Mollner, Donald Piper, Jack Ragland, Willard Schmidt, Carl Shy, Duane Swanson, Bill Wheatley and the trainer James Needles. Canada was runner-up; the games were played on an outdoor clay court. The first World Championship was held in Argentina in 1950.</p>
       </div>
      
       <div class="col-sm">
       <h4>NBA and Modern-day NBA</h4>
       <p>The Basketball league was founded in New York City on June 6, 1946 as the Basketball Association of America (BAA). The league adopted the name National Basketball Association (NBA) in 1949 after merging with the rival National Basketball League (NBL). As of the early 21st century, the NBA is the most significant professional basketball league in the world in terms of popularity, salaries, talent, and level of competition.</p>

       <p>The NBA has helped popularize basketball all over the world. A large part of this is due to the transcendent stars that have played the game through the years. It was because of the play of Michael Jordan that basketball started to reach international audiences, especially on the 1992 United States men's Olympic basketball team, known as the Dream Team.</p>
