      <h2>Kobe Bryant</h2>
      
      <p>Kobe Bean Bryant (born August 23, 1978) is an American former professional basketball player. He played his entire 20-year career with the Los Angeles Lakers of the National Basketball Association (NBA). He entered the NBA directly from high school and won five NBA championships with the Lakers. Bryant is an 18-time All-Star, 15-time member of the All-NBA Team, and 12-time member of the All-Defensive team. He led the NBA in scoring during two seasons, and ranks third on the league's all-time regular season scoring and fourth on the all-time postseason scoring list. He holds the NBA record for the most seasons playing with one franchise for an entire career and is widely regarded as one of the greatest basketball players of all time. Bryant is the first guard in NBA history to play for at least 20 seasons.Bryant was awarded the regular season's Most Valuable Player Award (MVP) in 2008. After losing in the 2008 NBA Finals, he led the Lakers to two consecutive championships in 2009 and 2010, earning the Finals MVP Award on both occasions.At 34 years and 104 days of age, Bryant became the youngest player in league history to reach 30,000 career points. He became the all-time leading scorer in Lakers franchise history on February 1, 2010, when he surpassed Jerry West. During his third year in the league, Bryant was chosen to start the All-Star Game, and he would continue to be selected to start that game for a record 18 consecutive appearances until his retirement. His four All-Star MVP Awards are tied for the most in NBA history. At the 2008 and 2012 Summer Olympics, he won gold medals as a member of the U.S. national team. Sporting News and TNT named Bryant the top NBA player of the 2000s. In 2018, Bryant won the Academy Award for Best Animated Short Film for his film Dear Basketball.</p>
